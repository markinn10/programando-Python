"""
# número = 1 / Recebe
+= número = numero + 1  Incremento
-= numero = numero / Decremento
*= numero = numero * 1 / Multplicação
/= numero = numero / 1 / Divisão
%= numero =  numero % 1 / Resto

"""

#operador =
numero = 10
print(numero)

#operador +=
numero += 1
print(numero)

#operador -=
numero -= 1
print(numero)

#operador *=
numero *= 2
print(numero)

#operador /=
numero /= 2
print(numero)

#operador %=
numero %= 4
print(numero)