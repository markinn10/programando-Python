"""
== Igual : verifica se um valor é igual ao outro
!= Diferente de : Verifica se um valor é diferente de outro
> Maior que : Verifica se um valor é maior que o outro
>= : Maior ou igual :  Verifica se um valor é maior ou igual ao outro
< menor que : \ Verifica se um valor é menor que outro
<= Menor ou igual : Verifica se um valor é menor ou igual outro
"""

# if - se

numero1 = 5
numero2 = 10
if numero1 == numero2:
    print("Ambos os numeros são iguais")


if numero1 != numero2:
    print("Os números são diferentes")

if numero1 > numero2:
    print("Numero 1 maior que o número 2")

if numero1 >= numero2:
    print("Numero 1 maior ou igual ao numero 2")

if numero1 < numero2:
    print("Numero 1 menor que o 2")






